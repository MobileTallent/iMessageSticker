#import "MessagesViewController.h"
#import "CollectionViewCell.h"

@interface MessagesViewController() {
    NSArray* stickerArray;
    NSArray* gifArray;
    NSUInteger total;
}

@end

@implementation MessagesViewController

- (void)viewDidLoad {
    
    [super viewDidLoad];
    
    //get the path and total Stickers
    NSString* path = [[NSBundle mainBundle] pathForResource:@"utilitylist" ofType:@"plist"];
    NSDictionary* dict = [[NSDictionary alloc] initWithContentsOfFile:path];
    stickerArray = [dict valueForKey:@"Pictures"];
    gifArray = [dict valueForKey:@"Gifs"];
  
    // Do any additional setup after loading the view.
}

#pragma mark -
#pragma mark - UICollectionViewDataSource

- (NSInteger)collectionView:(UICollectionView *)collectionView numberOfItemsInSection:(NSInteger)section{
    
    NSUInteger stickerCount = [stickerArray count];
    NSUInteger gifCount = [gifArray count];
    total = stickerCount + gifCount;
    return total;
}

- (UICollectionViewCell *)collectionView:(UICollectionView *)collectionView cellForItemAtIndexPath:(NSIndexPath *)indexPath{
    
    static NSString *identifier = @"Cell";
    NSURL *urlForSticker;
    
    CollectionViewCell *cell = [collectionView dequeueReusableCellWithReuseIdentifier:identifier forIndexPath:indexPath];

    if (indexPath.row < stickerArray.count) {
        
        urlForSticker = [[NSBundle mainBundle] URLForResource:[stickerArray objectAtIndex:indexPath.row] withExtension:@"png"];
    }
    else{
        
        urlForSticker = [[NSBundle mainBundle] URLForResource:[gifArray objectAtIndex:(indexPath.row - stickerArray.count)] withExtension:@"gif"];
    }
    
    cell.stickerView.sticker = [[MSSticker alloc] initWithContentsOfFileURL:urlForSticker localizedDescription:@"" error:nil];
    
    return cell;
    
}

#pragma mark -
#pragma mark - UICollectionViewFlowLayout

- (CGSize)collectionView:(UICollectionView *)collectionView layout:(UICollectionViewLayout*)collectionViewLayout sizeForItemAtIndexPath:(NSIndexPath *)indexPath
{
    
    CGFloat picDimension = self.view.frame.size.width / 4.0f;
    return CGSizeMake(picDimension, picDimension);
    
}

- (UIEdgeInsets)collectionView:(UICollectionView *)collectionView layout:(UICollectionViewLayout*)collectionViewLayout insetForSectionAtIndex:(NSInteger)section
{
    CGFloat leftRightInset = self.view.frame.size.width / 14.0f;
    return UIEdgeInsetsMake(0, leftRightInset, 0, leftRightInset);
}

